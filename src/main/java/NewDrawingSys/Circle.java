package NewDrawingSys;

/**
 * This is the class for the circles
 * @author erickcruz
 */
public class Circle {
    
    /**
     * Default/blank constructor for the circles
     */
    Circle() {
        
    }
    /**
     * The draw method that will be used to print out the circles data
     * @param x1
     * @param y1
     * @param r 
     */
    public void draw(int x1, int y1, int r) {
        System.out.println("Circle from center (" + x1 + ";" + y1 + "), with radius(" + r + ")");
    }

    
}
